/* Communication parameters */
#define ISR_PRIORITY_ASCLIN_SHELL_TX      8                               /* Priority for interrupt ISR Transmit  */
#define ISR_PRIORITY_ASCLIN_SHELL_RX      4                               /* Priority for interrupt ISR Receive   */
#define ISR_PRIORITY_ASCLIN_SHELL_ER      12                              /* Priority for interrupt ISR Errors    */
#define ASC_TX_SHELL_FIFO_SIZE            256                             /* Define the TX buffer size in byte    */
#define ASC_RX_SHELL_FIFO_SIZE            256                             /* Define the RX buffer size in byte    */
#define ASC_BAUDRATE                      9600                            /* Define the UART baud rate            */

/* LEDs */
#define LED1                              &MODULE_P00, 5                  /* LED1                                 */
#define LED2                              &MODULE_P00, 6                  /* LED2                                 */

/* Shell commands and help descriptions */
#define COMMAND_INFO                "info"
#define COMMAND_TOGGLE              "toggle"
#define COMMAND_HELP                "help"
#define COMMAND_INFO_HELP_TEXT      "   : Show the example's info"
#define COMMAND_TOGGLE_HELP_TEXT    " : Command to toggle LEDs" ENDLINE \
                                    "         The correct syntax for this" ENDLINE \
                                    "         command is '" COMMAND_TOGGLE " [0/1/2]'"
#define COMMAND_HELP_HELP_TEXT      "   : Show this help list"

#define ENDLINE     "\n"

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
/**
 * @print_shell_info
 * Command processing of shell "info" command. Wrapping of function print_info
 *
 * */
boolean print_shell_info(pchar args, void *data, IfxStdIf_DPipe *io);

/**
 * @print_toggle_leds
 * Command processing of shell "toggle" command
 *
 * */
boolean toggle_leds(pchar args, void *data, IfxStdIf_DPipe *io);

/**
 * @init_leds
 * Initialize board LEDs used by shell
 *
 * */
void init_leds(void);

/**
 * @print_info
 * Print info screen on shell
 *
 * */
void print_info(IfxStdIf_DPipe *io);


/**
 * @init_shell_UART
 * Initialize ASCLIN3 module for shell usage
 *
 * */
void init_shell_UART(void);

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxStdIf_DPipe  g_asclinPipeShell;                                      /* Standard interface object            */
IfxAsclin_Asc   g_asclinShellInterface;                                 /* ASCLIN module object                 */
Ifx_Shell       g_shellInterface;                                       /* Shell interface object               */

/* The transfer buffers allocate memory for the data itself and for FIFO runtime variables.
 * 8 more bytes have to be added to ensure a proper circular buffer handling independent from
 * the address to which the buffers have been located.
 */
uint8 g_uartTxShellFifo[ASC_TX_SHELL_FIFO_SIZE + sizeof(Ifx_Fifo) + 8];
uint8 g_uartRxShellFifo[ASC_RX_SHELL_FIFO_SIZE + sizeof(Ifx_Fifo) + 8];

/* Array that stores the supported Shell commands */
const Ifx_Shell_Command g_shellCommands[] = {
    {COMMAND_INFO,   COMMAND_INFO_HELP_TEXT,    &g_shellInterface, &print_shell_info },
    {COMMAND_TOGGLE, COMMAND_TOGGLE_HELP_TEXT,  &g_shellInterface, &toggle_leds },
    {COMMAND_HELP,   COMMAND_HELP_HELP_TEXT,    &g_shellInterface, &Ifx_Shell_showHelp },
    IFX_SHELL_COMMAND_LIST_END
};

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Macro to define Interrupt Service Routine.
 * This macro makes following definitions:
 * 1) Define linker section as .intvec_tc<vector number>_<interrupt priority>.
 * 2) define compiler specific attribute for the interrupt functions.
 * 3) define the Interrupt service routine as ISR function.
 *
 * IFX_INTERRUPT(isr, vectabNum, priority)
 *  - isr: Name of the ISR function.
 *  - vectabNum: Vector table number.
 *  - priority: Interrupt priority. Refer Usage of Interrupt Macro for more details.
 */
IFX_INTERRUPT(asc3_tx_ISR, 0, ISR_PRIORITY_ASCLIN_SHELL_TX);

void asc3_tx_ISR(void)
{
    IfxStdIf_DPipe_onTransmit(&g_asclinPipeShell);
}

IFX_INTERRUPT(asc3_rx_ISR, 0, ISR_PRIORITY_ASCLIN_SHELL_RX);

void asc3_rx_ISR(void)
{
    IfxStdIf_DPipe_onReceive(&g_asclinPipeShell);
}

IFX_INTERRUPT(asc3_err_ISR, 0, ISR_PRIORITY_ASCLIN_SHELL_ER);

void asc3_err_ISR(void)
{
    IfxStdIf_DPipe_onError(&g_asclinPipeShell);
}

void print_info(IfxStdIf_DPipe *io)
{
    IfxStdIf_DPipe_print(io, ENDLINE);
    IfxStdIf_DPipe_print(io, "***************************************"ENDLINE);
    IfxStdIf_DPipe_print(io, "This is an example that shows how to"ENDLINE);
    IfxStdIf_DPipe_print(io, "use the Infineon Shell on AURIX(TM)"ENDLINE);
    IfxStdIf_DPipe_print(io, "via Bluetooth Classic CYW920819"ENDLINE);
    IfxStdIf_DPipe_print(io, "device extension."ENDLINE);
    IfxStdIf_DPipe_print(io, "In order to toggle the LEDs enter   "ENDLINE);
    IfxStdIf_DPipe_print(io, "the command '" COMMAND_TOGGLE "'" ENDLINE);
    IfxStdIf_DPipe_print(io, "followed by one of the " ENDLINE);
    IfxStdIf_DPipe_print(io, "following parameters:"ENDLINE);
    IfxStdIf_DPipe_print(io, "  - '0': turns on all the LEDs on "ENDLINE);
    IfxStdIf_DPipe_print(io, "         the board "ENDLINE);
    IfxStdIf_DPipe_print(io, "  - '1': toggles the LED1"ENDLINE);
    IfxStdIf_DPipe_print(io, "  - '2': toggles the LED2"ENDLINE);
    IfxStdIf_DPipe_print(io, "Any other parameter turns off all "ENDLINE);
    IfxStdIf_DPipe_print(io, "the LEDs and reports a Shell command"ENDLINE);
    IfxStdIf_DPipe_print(io, "error. "ENDLINE);
    IfxStdIf_DPipe_print(io, "***************************************"ENDLINE);
}

boolean print_shell_info(pchar args, void *data, IfxStdIf_DPipe *io)
{
    print_info(io);
    return TRUE;
}

boolean toggle_leds(pchar args, void *data, IfxStdIf_DPipe *io)
{
    switch(args[0])
    {
        case '0': /* Turn on all LEDs */
            IfxPort_setPinState(LED1, IfxPort_State_low);
            IfxPort_setPinState(LED2, IfxPort_State_low);
            IfxStdIf_DPipe_print(io, "Turned on all LEDs!" ENDLINE ENDLINE);
            break;
        case '1': /* Toggle LED1 */
            IfxPort_setPinState(LED1, IfxPort_State_toggled);
            IfxStdIf_DPipe_print(io, "Toggled LED1!" ENDLINE ENDLINE);
            break;
        case '2': /* Toggle LED2 */
            IfxPort_setPinState(LED2, IfxPort_State_toggled);
            IfxStdIf_DPipe_print(io, "Toggled LED2!" ENDLINE ENDLINE);
            break;
        default: /* Turn off all LEDs */
            IfxPort_setPinState(LED1, IfxPort_State_high);
            IfxPort_setPinState(LED2, IfxPort_State_high);
            IfxStdIf_DPipe_print(io, "Command syntax not correct." ENDLINE \
                    "The correct syntax for this command is" ENDLINE "    '" COMMAND_TOGGLE " [0/1/2]'" ENDLINE \
                    "Turned off all LEDs!" ENDLINE);
            return FALSE; /* Returning false triggers a Shell command error */
    }
    return TRUE;
}

void init_leds(void)
{
    /* Initialize GPIO pins for LEDs */
    IfxPort_setPinMode(LED1, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(LED2, IfxPort_Mode_outputPushPullGeneral);

    /* Turn off all LEDs */
    IfxPort_setPinState(LED1, IfxPort_State_high);
    IfxPort_setPinState(LED2, IfxPort_State_high);
}

void init_shell_UART(void)
{
    IfxAsclin_Asc_Config ascConf;

    /* Initialize the config structure with default values */
    IfxAsclin_Asc_initModuleConfig(&ascConf, &MODULE_ASCLIN3);

    /* Set the desired baud rate and over sampling factor */
    ascConf.baudrate.baudrate = ASC_BAUDRATE;
    ascConf.baudrate.oversampling = IfxAsclin_OversamplingFactor_16;

    /* Configure the sampling mode */
    ascConf.bitTiming.medianFilter = IfxAsclin_SamplesPerBit_three;
    ascConf.bitTiming.samplePointPosition = IfxAsclin_SamplePointPosition_8;

    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins = {
            .cts        = NULL_PTR,
            .ctsMode    = IfxPort_InputMode_pullUp,
            .rx         = &IfxAsclin3_RXE_P00_1_IN,
            .rxMode     = IfxPort_InputMode_pullUp,
            .rts        = NULL_PTR,
            .rtsMode    = IfxPort_OutputMode_pushPull,
            .tx         = &IfxAsclin3_TX_P00_0_OUT,
            .txMode     = IfxPort_OutputMode_pushPull,
            .pinDriver  = IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConf.pins = &pins;

    /* FIFO buffers configuration */
    /* Set the transmission buffer pointer and size */
    ascConf.txBuffer = g_uartTxShellFifo;
    ascConf.txBufferSize = ASC_TX_SHELL_FIFO_SIZE;
    /* Set the receive buffer pointer and size */
    ascConf.rxBuffer = g_uartRxShellFifo;
    ascConf.rxBufferSize = ASC_RX_SHELL_FIFO_SIZE;

    /* ISR priorities and interrupt target */
    ascConf.interrupt.txPriority = ISR_PRIORITY_ASCLIN_SHELL_TX;
    ascConf.interrupt.rxPriority = ISR_PRIORITY_ASCLIN_SHELL_RX;
    ascConf.interrupt.erPriority = ISR_PRIORITY_ASCLIN_SHELL_ER;
    ascConf.interrupt.typeOfService = IfxSrc_Tos_cpu0;

    /* Initialize ASCLIN module with the given configuration */
    IfxAsclin_Asc_initModule(&g_asclinShellInterface, &ascConf);

    /* Initialize the Standard Interface */
    IfxAsclin_Asc_stdIfDPipeInit(&g_asclinPipeShell, &g_asclinShellInterface);
}

void init_shell(void)
{
    /* Initialize the hardware peripherals */
    init_shell_UART();
    init_leds();

    /* Print info to the console */
    print_info(&g_asclinPipeShell);
    IfxStdIf_DPipe_print(&g_asclinPipeShell, ENDLINE "Enter '" COMMAND_HELP "' to see the" ENDLINE);
    IfxStdIf_DPipe_print(&g_asclinPipeShell, " available commands" ENDLINE);

    /* Initialize the shell */
    /* Initialize the configuration structure with default values */
    Ifx_Shell_Config shellConf;
    Ifx_Shell_initConfig(&shellConf);

    /* Customize the configuration structure with example specific values */
    /* Set a pointer to the standard interface */
    shellConf.standardIo = &g_asclinPipeShell;
    /* Set the supported command list */
    shellConf.commandList[0] = &g_shellCommands[0];
    /* Disable echo */
    shellConf.echo = FALSE;

    /* Initialize the Shell with the given configuration */
    Ifx_Shell_init(&g_shellInterface, &shellConf);
}

void run_shell(void)
{
    /* Process the received data */
    Ifx_Shell_process(&g_shellInterface);
}
