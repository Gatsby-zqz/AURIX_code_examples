#include "LED.h"
#include "KIT_TC375_LK.h"

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
LED g_led1(&MODULE_P00, 5, true);                                                               /* On-board LED1    */
LED g_led2(&MODULE_P00, 6, true);                                                               /* On-board LED2    */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Function to turn ON the LED1 */
void turnOnLED1(void)
{
    g_led1.turnOn();
}

/* Function to turn ON the LED2 */
void turnOnLED2(void)
{
    g_led2.turnOn();
}

/* Function to turn OFF the LED1 */
void turnOffLED1(void)
{
    g_led1.turnOff();
}

/* Function to turn OFF the LED2 */
void turnOffLED2(void)
{
    g_led2.turnOff();
}

/* Function to toggle the LED1 */
void toggleLED1(void)
{
    g_led1.toggle();
}

/* Function to toggle the LED2 */
void toggleLED2(void)
{
    g_led2.toggle();
}
