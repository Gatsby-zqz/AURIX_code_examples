/* Use dedicated register files dependent on the used compiler */
#include "Scr_Io_sdcc.h"
#include "Scr_Scu_sdcc.h"
#include "Scr_Rtc_sdcc.h"
#include "Scr_Irq_sdcc.h"

void main(void)
{

    /* SCU (Standby Controller) module configurations*/
    SCR_SCU_PAGE = 1;          /* Switch to page 1          */
    SCR_PMCON1 = 0x39;         /* OCDS, RTC and SSC enabled */

    SCR_SCU_PAGE = 0;          /* Switch to page 0          */
    SCR_IRCON0 = 0x0;          /* Reset interrupts          */

    SCR_SCU_PAGE = 1;          /* Switch to page 1          */
    SCR_IEN0 = 0x80;           /* Enable global interrupts  */
    SCR_IEN1 = 0x80;           /* XINTR13 is enabled        */
    SCR_SCU_PAGE = 0;

    /* RTC (Real Time Clock) module configuration */
    /* Counter setting for 1s@100Mhz/DIV          */
    SCR_RTC_RTCCR3 = 0x01;
    SCR_RTC_RTCCR2 = 0x31;
    SCR_RTC_RTCCR1 = 0x2D;
    SCR_RTC_RTCCR0 = 0x00;
    /* RTC interrupt enable, RTC start operation, RTC 100MHz/DIV -> 20MHz, 9-bit prescaler is bypassed */
    SCR_RTC_RTCON =  0x17;

    /* IO (Port) module configuration                                                     */
    SCR_IO_PAGE = 2;           /* Switch to page 2                                        */
    SCR_P00_PDISC = 0x00;      /* Enable P0 all pins                                      */
    SCR_IO_PAGE = 0;           /* Switch to page 0                                        */
    SCR_P00_OUT = (1 << 4);    /* Initialize P00.4 (P33.4) out to 1 (LED on Triboard off) */
    SCR_IO_PAGE = 1;           /* Switch to page 1                                        */
    SCR_P00_IOCR4 = 0x80;      /* set P00.4 / P33.4 as output (LED on TriBoard)           */
    SCR_IO_PAGE = 0;

    while(1)
    {
    }
}

/* RTC Interrupt Service Routine (using SCR Interrupt Node 13) to toogle P00.4 (blink an LED on that port) */
void rtc_interrupt(void) __interrupt (XINTR13)
{
    unsigned char ioPage, scuPage;

    scuPage = SCR_SCU_PAGE; /* Save current value       */
    SCR_SCU_PAGE = 0;

    ioPage = SCR_IO_PAGE;   /* Save current value       */
    SCR_IO_PAGE = 0;        /* Switch to page 0         */

    SCR_P00_OMTR = 0x10;    /* Toogle P00.4             */
    SCR_IO_PAGE = ioPage;   /* Restore previous value   */
    SCR_SCU_PAGE = scuPage; /* Restore previous value   */
}
