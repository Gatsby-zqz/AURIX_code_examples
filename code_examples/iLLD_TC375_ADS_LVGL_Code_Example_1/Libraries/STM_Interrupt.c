/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "STM_Interrupt.h"
#include "Bsp.h"
#include "IfxPort.h"
#include "IfxStm.h"
#include "lvgl.h"

/*********************************************************************************************************************/
/*-----------------------------------------------------Macros--------------------------------------------------------*/
/*********************************************************************************************************************/
/* Priority for interrupt ISR */
#define ISR_PRIORITY_STM        40
/* Time between interrupts in 10ms */
#define TIMER_INT_TIME          10
/* STM0 is used in this example */
#define STM                     &MODULE_STM0

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
/* STM configuration structure */
IfxStm_CompareConfig g_stm_conf;
/* Variable to store the number of ticks to wait */
Ifx_TickTime g_ticks_for_10ms;
/* Variable to store occurrence of interrupt */
uint8_t g_stm_flag;

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementation-----------------------------------------------*/
/*********************************************************************************************************************/
/* Function to check whether interrupt occurred */
uint8_t check_stm_flag(void)
{
    return g_stm_flag;
}

/* Function to reset interrupt occurrence variable */
void reset_stm_flag(void)
{
    g_stm_flag = 0;
}

/* Macro to define Interrupt Service Routine */
IFX_INTERRUPT(isr_stm, 0, ISR_PRIORITY_STM);

/* Interrupt Service Routine */
void isr_stm(void)
{
    /* Update the compare register value that will trigger the next interrupt */
    IfxStm_increaseCompare(STM, g_stm_conf.comparator, g_ticks_for_10ms);

    lv_tick_inc(10);
    g_stm_flag = 1;
}

/* Function to initialize all the peripherals and variables used */
void init_stm_timer(void)
{
    /* Initialize time constant */
    g_ticks_for_10ms = IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, TIMER_INT_TIME);

    /* Initialize variable */
    g_stm_flag = 0;
    /* Initialize the configuration structure with default values */
    IfxStm_initCompareConfig(&g_stm_conf);

    /* Set the priority of the interrupt */
    g_stm_conf.triggerPriority = ISR_PRIORITY_STM;
    /* Set the service provider for the interrupts */
    g_stm_conf.typeOfService = IfxSrc_Tos_cpu0;
    /* Set the number of ticks after which the timer triggers an interrupt for the first time */
    g_stm_conf.ticks = g_ticks_for_10ms;
    /* Initialize the STM with the user configuration */
    IfxStm_initCompare(STM, &g_stm_conf);
}
