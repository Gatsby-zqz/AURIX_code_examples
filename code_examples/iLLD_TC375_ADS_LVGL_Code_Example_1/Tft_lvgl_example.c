#include "Tft_lvgl_example.h"
#include "lv_demo.h"
#include "lv_conf.h"

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototype-------------------------------------------------*/
/*********************************************************************************************************************/
static void create_hello_world(void);
static void btn_event_cb(lv_event_t * e);

/*********************************************************************************************************************/
/*--------------------------------------------------Local Variables--------------------------------------------------*/
/*********************************************************************************************************************/
static lv_style_t style_text_large;
static lv_style_t style_text_normal;

static const lv_font_t * font_large;
static const lv_font_t * font_normal;

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementation-----------------------------------------------*/
/*********************************************************************************************************************/
/* Function to initialize fonts, styles and call hello world screen */
void tft_lvgl_create_example(void)
{
    /* Set default font */
    font_large = LV_FONT_DEFAULT;
    font_normal = LV_FONT_DEFAULT;

#if LV_FONT_MONTSERRAT_20
        /* Set font */
        font_large     =  &lv_font_montserrat_20;
#endif
#if LV_FONT_MONTSERRAT_14
        /* Set font */
        font_normal    =  &lv_font_montserrat_14;
#endif

    /* Initialize a style */
    lv_style_init(&style_text_large);
    /* Configure style */
    lv_style_set_text_font(&style_text_large, font_large);

    /* Initialize a style */
    lv_style_init(&style_text_normal);
    /* Configure style */
    lv_style_set_text_font(&style_text_normal, font_normal);

    create_hello_world();
}

/* Function to create hello world screen */
static void create_hello_world(void)
{
    /* Add a screen */
    lv_obj_t * screen = lv_obj_create(NULL);
    /* Load screen */
    lv_scr_load(screen);

    /* Add a label to a screen */
    lv_obj_t * text_1 = lv_label_create(screen);
    /* Set the labels text */
    lv_label_set_text(text_1, "Hello World");
    /* Set its style */
    lv_obj_add_style(text_1, &style_text_large, 0);
    /* Set its position */
    lv_obj_set_pos(text_1, 10, 10);

    /* Add a label to a screen */
    lv_obj_t * text_2 = lv_label_create(screen);
    /* Set the labels text */
    lv_label_set_text(text_2, "Hello World");
    /* Set its style */
    lv_obj_add_style(text_2, &style_text_normal, 0);
    /* Set its position */
    lv_obj_set_pos(text_2, 10, 40);

    /* Add a button to a screen */
    lv_obj_t * btn = lv_btn_create(screen);
    /* Set its position */
    lv_obj_set_pos(btn, 10, 70);
    /* Set its size */
    lv_obj_set_size(btn, 150, 50);
    /* Assign a callback to the button */
    lv_obj_add_event_cb(btn, btn_event_cb, LV_EVENT_ALL, NULL);

    /* Add a label for the button */
    lv_obj_t * label = lv_label_create(btn);
    /* Set the labels text */
    lv_label_set_text(label, "Button");
    /* Set its alignment */
    lv_obj_center(label);
}

/* Callback function at touch event of a button
 * The button's label will be changed with every click event:
 * Number of click events are counted and shown */
static void btn_event_cb(lv_event_t * e)
{
    lv_event_code_t code = lv_event_get_code(e);
    lv_obj_t * btn = lv_event_get_target(e);
    if (code == LV_EVENT_CLICKED)
    {
        static uint8_t cnt = 0;
        cnt++;
        lv_obj_t * label = lv_obj_get_child(btn, 0);
        lv_label_set_text_fmt(label, "Clicked: %dx", cnt);
    }
}
